from functools import wraps
from flask_login import current_user, login_user, logout_user
from flask import abort, render_template, redirect, url_for, flash
from blog import app, db
from blog.models import BlogPost, User, Comment
from blog.forms import RegisterForm, LoginForm, CommentForm, CreatePostForm
from werkzeug.security import generate_password_hash, check_password_hash
from datetime import date


def admin_only(f):
    @wraps(f)
    def wrapper_function(*args, **kwargs):
        if current_user.id != 1:
            return abort(403)
        return f(*args, **kwargs)
    return wrapper_function


@app.route('/')
def get_all_posts():
    posts = BlogPost.query.all()
    return render_template("index.html", all_posts=posts)


@app.route('/register', methods=["GET", "POST"])
def register():
    form = RegisterForm()
    if form.validate_on_submit():
        if User.query.filter_by(email=form.email.data).first():
            flash("You've already signed up with this Email. Please log in!")
            return redirect(url_for("login"))
        hashed_password = generate_password_hash(
            password=form.password1.data,
            method="pbkdf2:sha256",
            salt_length=8,
        )

        new_user = User(
            username=form.username.data,
            email=form.email.data,
            password=hashed_password,
        )
        db.session.add(new_user)
        db.session.commit()
        login_user(new_user)
        return redirect(url_for("get_all_posts"))
    return render_template("register.html", form=form)


@app.route('/login', methods=["GET", "POST"])
def login():
    form = LoginForm()
    if form.validate_on_submit():
        email = form.email.data
        password = form.password.data
        user = User.query.filter_by(email=email).first()
        if not user:
            flash("The user with this Email does not exist.")
            return redirect(url_for("login"))
        elif not check_password_hash(user.password, password):
            flash("Wrong password, please try again.")
            return redirect(url_for("login"))
        else:
            login_user(user)
            return  redirect(url_for("get_all_posts"))
    return render_template("login.html", form=form)


@app.route('/logout')
def logout():
    logout_user()
    return redirect(url_for('get_all_posts'))


@app.route("/post/<int:post_id>", methods=["GET", "POST"])
def show_post(post_id):
    form = CommentForm()
    requested_post = BlogPost.query.get(post_id)
    if form.validate_on_submit():
        if not current_user.is_authenticated:
            flash("You need to login or register to comment.")
            return redirect(url_for("login"))
        new_comment = Comment(
            text=form.comment_text.data,
            comment_author = current_user,
            parent_post=requested_post
        )
        db.session.add(new_comment),
        db.session.commit()
    return render_template("post.html", post=requested_post, form=form, current_user=current_user)


